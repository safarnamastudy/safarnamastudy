// Function to populate Hotel Mgmt table
function populateHMTable() {
    const tbody = document.getElementById('hmCollegesTableBody');
    if (!tbody) return;

    tbody.innerHTML = '';

    hmColleges.forEach(college => {
        const row = document.createElement('tr');
        
        // Define badge color based on college type
        const typeBadgeColor = (college.type === 'Central Govt' || college.type === 'State Govt') ? '#f97316' : '#d97706';
        const typeBgColor = (college.type === 'Central Govt' || college.type === 'State Govt') ? '#ffedd5' : '#fffbeb';

        row.innerHTML = `
            <td class="rank-cell">
                <div class="rank-badge" style="border-color: #f97316; color: #c2410c; background: #fff7ed;">${college.rank}</div>
            </td>
            <td>
                <div class="college-name">${college.name}</div>
            </td>
            <td>
                <div class="college-location">${college.location}</div>
            </td>
            <td style="font-weight: 600; color: #db2777; font-size: 13px;">${college.entrance}</td>
            <td class="fee-cell">${college.fees}</td>
            <td class="placement-cell">${college.avgPlacement}</td>
            <td class="highest-placement-cell">${college.highestPlacement}</td>
            <td>
                <span style="background: ${typeBgColor}; color: ${typeBadgeColor}; padding: 4px 8px; border-radius: 4px; font-size: 12px; font-weight: 600;">
                    ${college.type}
                </span>
            </td>
            <td style="text-align: center;">
                <a href="college-site.html?id=HM-${college.rank}" class="site-btn" style="background: linear-gradient(135deg, #f97316 0%, #ea580c 100%);">View Site</a>
            </td>
        `;
        
        tbody.appendChild(row);
    });
    
    console.log('✅ Hotel Mgmt Colleges table populated with', hmColleges.length, 'colleges');
}

// Initialize when the DOM is fully loaded
document.addEventListener('DOMContentLoaded', function() {
    populateHMTable();
});